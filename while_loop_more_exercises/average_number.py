# Напишете програма, която прочита едно число n,
# след това прочита n на брой цели числа и принтира средно аритметичното на тяхната сума число,
# форматирано до втората цифра след десетични знак.
n = int(input())
sum = 0
for i in range(n):
    numbers = int(input())
    sum += numbers
print(f'{(sum/n):.2f}')
